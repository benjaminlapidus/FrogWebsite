	<h2>Feet and Legs</h2>

	<p>The structure of the feet and legs varies greatly among frog species, depending in part on whether they live primarily on the ground, in water, in trees or in burrows. Frogs must be able to move quickly through their environment to catch prey and escape predators, and numerous adaptations help them to do so. Most frogs are either proficient at jumping or are descended from ancestors that were, with much of the musculoskeletal morphology modified for this purpose. The tibia, fibula, and tarsals have been fused into a single, strong bone, as have the radius and ulna in the fore limbs (which must absorb the impact on landing). The metatarsals have become elongated to add to the leg length and allow frogs to push against the ground for a longer period on take-off. The illium has elongated and formed a mobile joint with the sacrum which, in specialist jumpers such as ranids and hylids, functions as an additional limb joint to further power the leaps. The tail vertebrae have fused into a urostyle which is retracted inside the pelvis. This enables the force to be transferred from the legs to the body during a leap.</p>

