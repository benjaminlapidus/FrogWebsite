	<h2>Muscles</h2>

	<p>The muscular system has been similarly modified. The hind limbs of ancestral frogs presumably contained pairs of muscles which would act in opposition (one muscle to flex the knee, a different muscle to extend it), as is seen in most other limbed animals. However, in modern frogs, almost all muscles have been modified to contribute to the action of jumping, with only a few small muscles remaining to bring the limb back to the starting position and maintain posture. The muscles have also been greatly enlarged, with the main leg muscles accounting for over 17% of the total mass of frogs.</p>

	<p>Many frogs have webbed feet and the degree of webbing is directly proportional to the amount of time the species spends in the water. The completely aquatic African dwarf frog (Hymenochirus sp.) has fully webbed toes, whereas those of White's tree frog (Litoria caerulea), an arboreal species, are only a quarter or half webbed. Exceptions include flying frogs in the Hylidae and Rhacophoridae, which also have fully webbed toes used in gliding.</p>

