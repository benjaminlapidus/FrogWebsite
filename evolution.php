	<h2>Early Frogs</h2>

	<p>In 2008, Gerobatrachus hottoni, a temnospondyl with many frog- and salamander-like characteristics, was discovered in Texas. It dated back 290 million years and was hailed as a missing link, a stem batrachian close to the common ancestor of frogs and salamanders, consistent with the widely accepted hypothesis that frogs and salamanders are more closely related to each other (forming a clade called Batrachia) than they are to caecilians. However, others have suggested that Gerobatrachus hottoni was only a dissorophoid temnospondyl unrelated to extant amphibians.</p>

	<p>Salientia (Latin salere (salio), "to jump") is the name of the total group that includes modern frogs in the order Anura as well as their close fossil relatives, the "proto-frogs" or "stem-frogs". The common features possessed by these proto-frogs include 14 presacral vertebrae (modern frogs have eight or 9), a long and forward-sloping ilium in the pelvis, the presence of a frontoparietal bone, and a lower jaw without teeth. The earliest known amphibians that were more closely related to frogs than to salamanders are Triadobatrachus massinoti, from the early Triassic period of Madagascar (about 250 million years ago), and Czatkobatrachus polonicus, from the Early Triassic of Poland (about the same age as Triadobatrachus). The skull of Triadobatrachus is frog-like, being broad with large eye sockets, but the fossil has features diverging from modern frogs. These include a longer body with more vertebrae. The tail has separate vertebrae unlike the fused urostyle or coccyx in modern frogs. The tibia and fibula bones are also separate, making it probable that Triadobatrachus was not an efficient leaper.</p>

